package com.yikang.protal.entity;
/**
 * 这个类是日注册数量的表格上面显示的内容
 * @author dell
 *
 */
public class Count {
	//	日期
	private String checkDay;
	//	数量
	private int count;



	public String getCheckDay() {
		return checkDay;
	}

	public void setCheckDay(String checkDay) {
		this.checkDay = checkDay;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}
}
